function mostrarIdade(name, age){
    return `A idade de ${name} é ${age}`;
}

function mostrarCidade(name, city){
    return `A cidade de ${name} é ${city}`;
}

function mostrarHobby(name, hobby){
    return `O passa-tempo de ${name} é ${hobby}`;
}

export{
    mostrarIdade,
    mostrarCidade,
    mostrarHobby
}